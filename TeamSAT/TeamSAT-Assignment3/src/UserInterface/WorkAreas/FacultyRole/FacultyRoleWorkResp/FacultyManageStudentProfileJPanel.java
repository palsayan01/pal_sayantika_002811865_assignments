    private void BtnGradeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGradeActionPerformed
        // TODO add your handling code here:
        String grade = txtManageStudentProfileGrade.getText();
        String gradePattern = "^[A-Fa-f]$";
        if (grade.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a grade.");
        } else if (!grade.matches(gradePattern)) {
            JOptionPane.showMessageDialog(this, "Invalid grade. Please enter a valid grade (e.g., A, B, C, etc.).");
        } else {
            
            courseLoad.setGrade(txtManageStudentProfileGrade.getText());
            JOptionPane.showMessageDialog(this, "Grades are saved successfully!");
        }

    }//GEN-LAST:event_BtnGradeActionPerformed

