    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblListStudent.getSelectedRow();

        if (selectedRowIndex < 0) {

            JOptionPane.showMessageDialog(this, "Please select a row to view.");
            return;
        }
        else
        {
        DefaultTableModel model = (DefaultTableModel) tblListStudent.getModel();
        courseLoad = (CourseLoad) model.getValueAt(selectedRowIndex, 6);
        
        if(courseLoad.getStudentProfile().getStatus().equals("Hired") || courseLoad.getStudentProfile().getTranscript().getGradStatus().equals("Graduated"))
        {
            BtnGrade.setEnabled(false);
            txtManageStudentProfileGrade.setText(courseLoad.getGrade());
            txtManageStudentProfileGrade.setEditable(false);
            txtFeedback.setText(courseLoad.getStudentProfile().getFeedback());

        }
        
        txtManageStudentProfileStudentID.setText(String.valueOf(courseLoad.getStudentProfile().getStudentId()));
        txtManageStudentProfileName.setText(courseLoad.getStudentProfile().getPerson().getName());
        txtManageStudentProfileCRN.setText(String.valueOf(courseLoad.getCourseOffer().getCrn()));
        txtManageStudentProfileCourseName.setText(courseLoad.getCourseOffer().getCourse().getCourseName());
        txtManageStudentProfileTerm.setText(courseLoad.getCourseOffer().getTerm());
        txtManageStudentProfileYear.setText(String.valueOf(courseLoad.getCourseOffer().getYear()));
        
        
        }

    }//GEN-LAST:event_btnViewActionPerformed

