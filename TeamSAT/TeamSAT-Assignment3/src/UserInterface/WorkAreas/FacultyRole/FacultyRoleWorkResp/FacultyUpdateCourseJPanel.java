    public FacultyUpdateCourseJPanel(Component previousScreen, JSplitPane splitPane, CourseOffer courseOffer, CourseDirectory cd, CourseOfferDirectory cod, FacultyProfile f, CourseLoadDirectory courseLoadDirectory) {
        this.previousScreen = previousScreen;
        this.splitPane = splitPane;
        this.courseOffer = courseOffer;
        this.cd = cd;
        this.cod = cod;
        this.f = f;
        this.courseLoadDirectory=courseLoadDirectory;
        initComponents();

        txtUpdateCourseID.setText(String.valueOf(courseOffer.getCrn()));
        txtUpdateCourseName.setText(courseOffer.getCourse().getCourseName());
        txtUpdateCourseDescription.setText(courseOffer.getCourse().getDescription());
        txtUpdateCourseCredits.setText(String.valueOf(courseOffer.getCourse().getCredit()));
        txtUpdateCourseTerm.setText(courseOffer.getTerm());
        txtUpdateCourseYear.setText(String.valueOf(courseOffer.getYear()));
        txtUpdateCourseLanguage.setText(courseOffer.getCourse().getLanguage());

        String schedule = courseOffer.getSchedule();

        Map<String, ArrayList<String>> scheduleMap = new HashMap<>();
        String[] parts = schedule.split(" ");

        { // This code decodes schedule from single string to each daywise strings
            String currentDay = null;
            ArrayList<String> currentSchedule = new ArrayList<>();

            // Iterate through the parts and extract day and time information
            for (String part : parts) {
                if (part.equals("Monday") || part.equals("Tuesday") || part.equals("Wednesday") || part.equals("Thursday") || part.equals("Friday") || part.equals("Saturday")) {
                    if (currentDay != null) {
                        scheduleMap.put(currentDay, currentSchedule);
                    }
                    currentDay = part;
                    currentSchedule = new ArrayList<>();
                } else {
                    currentSchedule.add(part);
                }
            }

// Add the last day's schedule
            if (currentDay != null) {
                scheduleMap.put(currentDay, currentSchedule);
            }

        }

// Access individual variables for each day
        txtUpdateMondaySchedule.setText(String.join(" ", scheduleMap.getOrDefault("Monday", new ArrayList<>())));
        txtUpdateTuesdaySchedule.setText(String.join(" ", scheduleMap.getOrDefault("Tuesday", new ArrayList<>())));
        txtUpdateCourseWednesday.setText(String.join(" ", scheduleMap.getOrDefault("Wednesday", new ArrayList<>())));
        txtUpdateCourseThursday.setText(String.join(" ", scheduleMap.getOrDefault("Thursday", new ArrayList<>())));
        txtUpdateCourseFriday.setText(String.join(" ", scheduleMap.getOrDefault("Friday", new ArrayList<>())));
        txtUpdateCourseSaturday.setText(String.join(" ", scheduleMap.getOrDefault("Saturday", new ArrayList<>())));

    }

