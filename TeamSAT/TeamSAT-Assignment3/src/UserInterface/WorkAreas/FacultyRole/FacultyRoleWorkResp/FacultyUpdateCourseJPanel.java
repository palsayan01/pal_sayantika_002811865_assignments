    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (txtUpdateCourseID.getText().isEmpty() || txtUpdateCourseName.getText().isEmpty()
                || txtUpdateCourseDescription.getText().isEmpty() || txtUpdateCourseCredits.getText().isEmpty()
                || txtUpdateCourseTerm.getText().isEmpty() || txtUpdateCourseYear.getText().isEmpty()
                || txtUpdateCourseLanguage.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter all values.");
            return;
        }

        if (!txtUpdateCourseID.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Course ID must be a numeric value.");
            return;
        }

        if (!txtUpdateCourseCredits.getText().matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(this, "Credits must be a numeric value.");
            return;
        }

        if (!txtUpdateCourseYear.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Year must be a numeric value.");
            return;
        }

        courseOffer.setCrn(Integer.parseInt(txtUpdateCourseID.getText()));
        courseOffer.setTerm(txtUpdateCourseTerm.getText());
        courseOffer.getCourse().setCourseName(txtUpdateCourseName.getText());
        courseOffer.getCourse().setDescription(txtUpdateCourseDescription.getText());
        courseOffer.getCourse().setCredit(Float.parseFloat(txtUpdateCourseCredits.getText()));
        courseOffer.getCourse().setLanguage(txtUpdateCourseLanguage.getText());
        courseOffer.setYear(Integer.parseInt(txtUpdateCourseYear.getText()));

String schedule = "";
 if (!txtUpdateMondaySchedule.getText().isBlank()) {
            schedule += "Monday " +  txtUpdateMondaySchedule.getText() + " ";
        }
        if (!txtUpdateTuesdaySchedule.getText().isBlank()) {
            schedule += "Tuesday " + txtUpdateTuesdaySchedule.getText() + " ";
        }
        if (!txtUpdateCourseWednesday.getText().isBlank()) {
            schedule += "Wednesday " + txtUpdateCourseWednesday.getText() + " ";
        }
        if (!txtUpdateCourseThursday.getText().isBlank()) {
            schedule += "Thursday " + txtUpdateCourseThursday.getText() + " ";
        }
        if (!txtUpdateCourseFriday.getText().isBlank()) {
            schedule += "Friday " + txtUpdateCourseFriday.getText() + " ";
        }
        if (!txtUpdateCourseSaturday.getText().isBlank()) {
            schedule += "Saturday " + txtUpdateCourseSaturday.getText() + " ";
        }
        

        courseOffer.setSchedule(schedule);
        JOptionPane.showMessageDialog(this, "Course information is updated.");
    }//GEN-LAST:event_btnSaveActionPerformed


